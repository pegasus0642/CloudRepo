package Team07.com.user;

public class Reply {


    private int replyId;
    private String replyInfo;
    private int replyQuestion;
    private int replyUser;
    private String replyTime;
    private String replyusername;
    public int getReplyId() {
        return replyId;
    }
    public void setReplyId(int replyId) {
        this.replyId = replyId;
    }
    public String getReplyInfo() {
        return replyInfo;
    }
    public void setReplyInfo(String replyInfo) {
        this.replyInfo = replyInfo;
    }
    public int getReplyUser() {
        return replyUser;
    }
    public void setReplyUser(int replyUser) {
        this.replyUser = replyUser;
    }
    public int getReplyQuestion() {
        return replyQuestion;
    }
    public void setReplyQuestion(int replyQuestion) {
        this.replyQuestion = replyQuestion;
    }
    public String getReplyTime() {
        return replyTime;
    }
    public void setReplyTime(String replyTime) {
        this.replyTime = replyTime;
    }
    public String getReplyusername() {
        return replyusername;
    }
    public void setReplyusername(String replyusername) {
        this.replyusername = replyusername;
    }


}
