package Team07.com.user;

public class Question {

    private int questionId;
    private String questionInfo;
    private int questionUser;
    private int questionBestReply;
    private String questionUsername;
    private String questiontime;
    public int getQuestionId() {
        return questionId;
    }
    public void setQuestionId(int questionId) {
        this.questionId = questionId;
    }
    public String getQuestionInfo() {
        return questionInfo;
    }
    public void setQuestionInfo(String questionInfo) {
        this.questionInfo = questionInfo;
    }
    public int getQuestionUser() {
        return questionUser;
    }
    public void setQuestionUser(int questionUser) {
        this.questionUser = questionUser;
    }
    public int getQuestionBestReply() {
        return questionBestReply;
    }
    public void setQuestionBestReply(int questionBestReply) {
        this.questionBestReply = questionBestReply;
    }
    public String getQuestionUsername() {
        return questionUsername;
    }
    public void setQuestionUsername(String questionUsername) {
        this.questionUsername = questionUsername;
    }
    public String getQuestiontime() {
        return questiontime;
    }
    public void setQuestiontime(String questiontime) {
        this.questiontime = questiontime;
    }


}
